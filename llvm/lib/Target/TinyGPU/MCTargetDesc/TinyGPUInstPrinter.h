#ifndef LLVM_LIB_TARGET_TinyGPU_INSTPRINTER_TinyGPUINSTPRINTER_H
#define LLVM_LIB_TARGET_TinyGPU_INSTPRINTER_TinyGPUINSTPRINTER_H

#include "llvm/MC/MCInstPrinter.h"

namespace llvm {

class TinyGPUInstPrinter : public MCInstPrinter {
public:
  TinyGPUInstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                  const MCRegisterInfo &MRI);

  // Autogenerated by tblgen.
  virtual std::pair<const char *, uint64_t> getMnemonic(const MCInst &MI) const override;
  void printInstruction(const MCInst *MI, uint64_t Address, raw_ostream &O);
  static const char *getRegisterName(MCRegister Reg);

  // Overriden
  void printRegName(raw_ostream &OS, MCRegister RegNo) override;
  void printInst(const MCInst *MI, uint64_t Address, StringRef Annot,
                 const MCSubtargetInfo &STI, raw_ostream &O) override;
  
  // Used in generated code
  bool printAliasInstr(const MCInst *MI, uint64_t Address, raw_ostream &OS);
  void printCustomAliasOperand(const MCInst *MI, uint64_t Address,
                               unsigned OpIdx, unsigned PrintMethodIdx,
                               raw_ostream &O);
  
private:
  void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O);
};
} // end namespace llvm

#endif // end LLVM_LIB_TARGET_TinyGPU_INSTPRINTER_TinyGPUINSTPRINTER_H
